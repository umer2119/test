import time

def print_big_letters():
    u = [
        "||          ||",
        "||          ||",
        "||          ||",
        "||          ||",
        "||          ||",
        "||          ||",
        " |||||||||||| ",
        "  ||||||||||  "
    ]

    m = [
        "||          ||",
        "|||        |||",
        "||||      ||||",
        "|| ||    || ||",
        "||  ||  ||  ||",
        "||   ||||   ||",
        "||          ||",
        "||          ||"
    ]

    e = [
        " |||||||||||||| ",
        " |||||||||||||| ",
        " ||             ",
        " ||||||||||||   ",
        " ||||||||||||   ",
        " ||             ",
        " |||||||||||||| ",
        " |||||||||||||| "
    ]

    r = [
        " |||||||||||||  ",
        " |||||||||||||| ",
        " ||          || ",
        " |||||||||||||  ",
        " ||||||||||||   ",
        " ||   ||        ",
        " ||    ||       ",
        " ||     ||      "
    ]

    name = [u, m, e, r]
    for i in range(8):
        for letter in name:
            print(letter[i], end="   ")
        print()
        time.sleep(0.5)  # delay of 0.5 seconds

print_big_letters()
